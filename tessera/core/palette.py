GRAY_RAMPS = ["graphite", "pewter", "marble", "obsidian", "bone"]
COLOR_RAMPS = [k for k in RAMPS if k not in GRAY_RAMPS]


def apply_ramp(t: np.ndarray, ramp) -> np.ndarray:
    """t HxW in [0,1] -> HxWx3."""
    if isinstance(ramp, str):
        ramp = RAMPS[ramp]
    xs = np.array([p[0] for p in ramp])
    cols = np.array([p[1] for p in ramp])
    tc = np.clip(t, 0.0, 1.0)
    out = np.empty(t.shape + (3,))
    for c in range(3):
        out[..., c] = np.interp(tc, xs, cols[:, c])
    return out
